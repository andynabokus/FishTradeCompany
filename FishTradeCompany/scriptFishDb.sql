FISHCOMPANY.CUSTORDER.ID = FISHCOMPANY.CONFIRMEDPAYMENT.APPRORDERID
AND
FISHCOMPANY.FISHSTORE.ID = FISHCOMPANY.CUSTORDER.FISHID
AND
FISHCOMPANY.CONFIRMEDPAYMENT.SHIPMENTAVAILABLE LIKE 'Y'
AND FISHCOMPANY.CUSTORDER.ORDERDATE BETWEEN '2015-08-01' AND '2015-10-01'
GROUP BY FISHCOMPANY.FISHSTORE.FISHNAME,FISHCOMPANY.CUSTORDER.ORDERDATE;




