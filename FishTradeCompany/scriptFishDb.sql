SELECT FISHCOMPANY.FISHSTORE.FISHNAME FROM FISHCOMPANY.FISHSTORE, FISHCOMPANY.CUSTORDER
WHERE FISHCOMPANY.CUSTORDER.FISHID = FISHCOMPANY.FISHSTORE.ID
