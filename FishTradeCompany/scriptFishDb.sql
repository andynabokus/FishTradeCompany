SELECT FISHCOMPANY.CUSTORDER.ORDERDATE,SUM((FISHCOMPANY.CUSTORDER.COPYSALEPRICE * FISHCOMPANY.CUSTORDER.WEIGHT) - (FISHCOMPANY.FISHSTORE.DELIVERYPRICE * FISHCOMPANY.CUSTORDER.WEIGHT)) as income
FROM FISHCOMPANY.FISHSTORE, FISHCOMPANY.CUSTORDER
WHERE FISHCOMPANY.CUSTORDER.ORDERNUM = ANY
(SELECT FISHCOMPANY.CONFIRMEDPAYMENT.ORDERNUM from FISHCOMPANY.CONFIRMEDPAYMENT
WHERE FISHCOMPANY.CONFIRMEDPAYMENT.SHIPMENTAVAILABLE LIKE 'Y')
AND FISHCOMPANY.CUSTORDER.FISHID = FISHCOMPANY.FISHSTORE.ID
AND FISHCOMPANY.CUSTORDER.ORDERDATE BETWEEN '2014-08-01' AND '2015-12-31' 
AND FISHCOMPANY.CUSTORDER.ORDERNUM = ANY
(SELECT FISHCOMPANY.CONFIRMEDPAYMENT.ORDERNUM from FISHCOMPANY.CONFIRMEDPAYMENT
WHERE FISHCOMPANY.CONFIRMEDPAYMENT.SHIPMENTAVAILABLE LIKE 'Y')
GROUP BY ORDERDATE
ORDER BY income asc;

