SELECT FISHCOMPANY.FISHSTORE.FISHNAME,FISHCOMPANY.CUSTORDER.ORDERDATE,
SUM(FISHCOMPANY.CUSTORDER.WEIGHT) AS WEIGHT,
SUM(FISHCOMPANY.CUSTORDER.COPYSALEPRICE) AS INCOME
